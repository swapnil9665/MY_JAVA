import java.util.*;
class Op5{
public static void main(String args[]){

	Scanner sc = new Scanner(System.in);
	System.out.println("Initial value of num1:- ");
	int num1=sc.nextInt();
	System.out.println("Initial value of num2:- ");
	int num2=sc.nextInt();

	System.out.println("after num1+=3,  num1="+(num1+=3));
	System.out.println("after num2-=2,  num2="+(num2-=2));
	System.out.println("after num1*=2,  num1="+(num1*=2));
	System.out.println("after num2/=3,  num2="+(num2/=3));
	System.out.println("after num1%=5,  num1="+(num1%=5));
	



}
}
